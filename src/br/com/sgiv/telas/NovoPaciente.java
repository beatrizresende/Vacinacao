    private int buscarId(JComboBox<String> cbx, String tabela) throws SQLException {
        String sqlId = "SELECT id FROM " + tabela + " WHERE nome=?";
        PreparedStatement pstId = conexao.prepareStatement(sqlId);
        pstId.setString(1, cbx.getSelectedItem().toString());
        rs = pstId.executeQuery();
        int id = 0;
        while (rs.next()) {
            id = rs.getInt(1);
        }
        return id;
    }

    private void criarPaciente() throws SQLException {
        Date dataSQL = null;
        String sql = "INSERT INTO paciente VALUES(NULL,?,?,?,?,?,?,?)";
        PreparedStatement pst = conexao.prepareStatement(sql);
        pst.setString(1, txtNome.getText());
        String data = txtData.getText();
        try {
            SimpleDateFormat formatoData = new SimpleDateFormat("yyyy-mm-dd");
            java.util.Date dataFormatada = formatoData.parse(data);
            dataSQL = new Date(dataFormatada.getTime());
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        pst.setDate(2, dataSQL);
        pst.setString(3, txtTelefone.getText());
        pst.setString(4, txtEndereco.getText());
        pst.setInt(5, buscarId(cbxRegiao, "regiao"));
        pst.setInt(6, buscarId(cbxEscolaridade, "escolaridade"));
        pst.setString(7, txtCPF.getText());
        pst.execute();
    }

